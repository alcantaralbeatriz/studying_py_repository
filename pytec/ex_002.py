#02. Faça um programa que receba 2 números inteiros e realize as 4 operações. Imprima o resultado das operações.

def pytec002():
    n1 = int(input('Forneça um número: '))
    n2 = int(input('Forneça outro número: '))
    print(f'Soma entre {n1} e {n2} é {n1+n2}')
    print(f'Subtração entre {n1} e {n2} é {n1-n2}')
    print(f'Multiplicação entre {n1} e {n2} é {n1*n2}')
    print(f'Divisão entre {n1} e {n2} é {n1/n2}')

pytec002()

